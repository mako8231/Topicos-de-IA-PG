#include "funcoes.h"

float soma(float x1, float x2){
    return x1 + x2;
}

float subtracao(float x1, float x2){
    return x1 - x2;
}

float multiplicacao(float x1, float x2){
    return x1 * x2;
}

float divisao(float x1, float x2){
    return x1/x2;
}

float resto(float x1, float x2){
    return (int)x1 % (int)x2;
}